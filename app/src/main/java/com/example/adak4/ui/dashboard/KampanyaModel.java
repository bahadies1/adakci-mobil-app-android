package com.example.adak4.ui.dashboard;

public class KampanyaModel {

    private String kampanyaName ;
    private String kampanyaImage;
    private String kampanyaKey;
    private String kampanyaCode;
    private String kampanyaTitle;
    private String kampanyaTitleImage;
    private String kampanyaDescription;


    public KampanyaModel() {

    }

    public String getKampanyaName() {
        return kampanyaName;
    }

    public void setKampanyaName(String kampanyaName) {
        this.kampanyaName = kampanyaName;
    }

    public String getKampanyaImage() {
        return kampanyaImage;
    }

    public void setKampanyaImage(String kampanyaImage) {
        this.kampanyaImage = kampanyaImage;
    }

    public String getKampanyaKey() {
        return kampanyaKey;
    }

    public void setKampanyaKey(String kampanyaKey) {
        this.kampanyaKey = kampanyaKey;
    }

    public String getKampanyaCode() {
        return kampanyaCode;
    }

    public void setKampanyaCode(String kampanyaCode) {
        this.kampanyaCode = kampanyaCode;
    }

    public String getKampanyaTitle() {
        return kampanyaTitle;
    }

    public void setKampanyaTitle(String kampanyaTitle) {
        this.kampanyaTitle = kampanyaTitle;
    }

    public String getKampanyaTitleImage() {
        return kampanyaTitleImage;
    }

    public void setKampanyaTitleImage(String kampanyaTitleImage) {
        this.kampanyaTitleImage = kampanyaTitleImage;
    }

    public String getKampanyaDescription() {
        return kampanyaDescription;
    }

    public void setKampanyaDescription(String kampanyaDescription) {
        this.kampanyaDescription = kampanyaDescription;
    }
}
