package com.example.adak4.ui.home.menu.supermenu;


public class SuperMenuModel {

    private String supermenuDescription;
    private String supermenuDescriptionTitle;
    private String supermenuImage;
    private String supermenuName;
    private String supermenuKey;
    private String supermenuTitleImage;

    public SuperMenuModel() {

    }

    public String getSupermenuDescription() {
        return supermenuDescription;
    }

    public void setSupermenuDescription(String supermenuDescription) {
        this.supermenuDescription = supermenuDescription;
    }

    public String getSupermenuDescriptionTitle() {
        return supermenuDescriptionTitle;
    }

    public void setSupermenuDescriptionTitle(String supermenuDescriptionTitle) {
        this.supermenuDescriptionTitle = supermenuDescriptionTitle;
    }

    public String getSupermenuImage() {
        return supermenuImage;
    }

    public void setSupermenuImage(String supermenuImage) {
        this.supermenuImage = supermenuImage;
    }

    public String getSupermenuName() {
        return supermenuName;
    }

    public void setSupermenuName(String supermenuName) {
        this.supermenuName = supermenuName;
    }

    public String getSupermenuKey() {
        return supermenuKey;
    }

    public void setSupermenuKey(String supermenuKey) {
        this.supermenuKey = supermenuKey;
    }

    public String getSupermenuTitleImage() {
        return supermenuTitleImage;
    }

    public void setSupermenuTitleImage(String supermenuTitleImage) {
        this.supermenuTitleImage = supermenuTitleImage;
    }
}
